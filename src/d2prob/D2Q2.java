package d2prob;

import java.util.Scanner;

public class D2Q2 {
    public static void main(String[] args) {
        int count = 10;
        System.out.println(++count);  // 11
        System.out.println(count++);  // 11 12
        System.out.println(--count);  // 11
        System.out.println(count--);  // 11 10

    }
}
